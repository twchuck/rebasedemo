package main;

public class Calculator {

	public static int add(int a, int b) {
		return a + b + b + b;
	}

	public static int sub(int a, int b) {
		return a - b - b - b - b;
	}

	public static int multiply(int a, int b) {
		return a * b * b;
	}

	public static int div(int a, int b) {
		return a / b / b;
	}
}
